const hmoName = (p: Profile | null): string => {
  switch (p?.hmo) {
    case 'clalit':
      return 'שירותי בריאות כללית'
    case 'maccabi':
      return 'מכבי שירותי בריאות'
    case 'meuhedet':
      return 'קופת חולים מאוחדת'
    case 'leumit':
      return 'לאומית שירותי בריאות'
    default:
      return 'קופת החולים'
  }
}

const signature = (d: LetterDetails) =>
  `בכבוד רב,\n${d.fullName || '[שם מלא]'}\nת.ז. ${d.idNumber || '[מספר ת.ז.]'}\nטלפון: ${d.phone || '[טלפון]'}\nכתובת: ${d.address || '[כתובת]'}`

const childLine = (d: LetterDetails) =>
  `${d.childName || '[שם הילד/ה]'}, ת.ז. ${d.childIdNumber || '[ת.ז. הילד/ה]'}`

